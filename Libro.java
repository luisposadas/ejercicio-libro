
public class  Libro
{
    private String titulo;
    private String autor;
    public Libro(String tituloi,String autori)
    {
        titulo = tituloi;
        autor = autori;
    }
    public String dimeAutor()
    {
        return autor;
    }
    public String dimeTitulo()
    {
        return titulo;
    }
    
}